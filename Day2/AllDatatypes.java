package Day2;

class AllTypes {
    public static void main(String[] args) {
        int a = 10;
        float f = 2.5f;
        double d = 5.55;
        char c = 'J';
        boolean b = true;

        System.out.println(a);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
}
